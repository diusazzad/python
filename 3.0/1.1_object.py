# Python-এ সবকিছুই অবজেক্ট (Object)
# প্রতিটি অবজেক্টের তিনটি বৈশিষ্ট্য:
# ১. Identity (আইডেন্টিটি) - মেমরি ঠিকানা
# ২. Type (টাইপ) - অবজেক্টের প্রকার
# ৩. Value (ভ্যালু) - অবজেক্টের মান

# উদাহরণ:
x = 10
y = "hello"
z = [1, 2, 3]

print(f"x: id={id(x)}, type={type(x)}, value={x}")
print(f"y: id={id(y)}, type={type(y)}, value={y}")
print(f"z: id={id(z)}, type={type(z)}, value={z}")
